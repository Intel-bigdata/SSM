package org.smartdata.server.actions;

import org.smartdata.common.actions.ActionType;

import java.util.UUID;
import java.util.concurrent.Semaphore;

/**
 * Controls the concurrency of actions execution.
 */
final public class ActionExecutor {
  // To control the concurrency of certain type of action
  static private Semaphore[] semaphores = new Semaphore[ActionType.values().length];

  static {
    // TODO: make configurable
    // TODO: Add user defined here
    semaphores[ActionType.BalanceCluster.getValue()] = new Semaphore(1);
  }

  public static UUID run(ActionBase action) {
    int v = action.getActionType().getValue();
    boolean acquired = false;
    try {
      if (semaphores[v] != null) {
        semaphores[v].acquire();
      }
      acquired = true;
      return action.run();
    } catch (InterruptedException e) {
      e.printStackTrace();
      return null;
    } finally {
      if (acquired && semaphores[v] != null) {
        semaphores[v].release();
      }
    }
  }
}
